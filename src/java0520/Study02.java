package java0520;

public class Study02 {

	public static void main(String[] args) {
		
		// 구구단
		// 단 * 증가값 = 결과값
		
		for (int 단 = 1; 단 <= 1; 단++) { // 단를 위한 반복문
			
			for(int 증가값 = 1; 증가값 <= 9; 증가값++ ) { // 증가값 위한 반복문 1 ~ 9 
				
				// 출력 하는 곳
				System.out.println(단 + " * " + 증가값 + " = " + (단 * 증가값));
				
			}
			
		}

	}

}
